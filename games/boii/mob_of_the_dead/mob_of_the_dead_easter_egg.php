                <h3>Step 5: Listening to the Recordings</h3>
                <p>At this point, you are almost done with the Easter Egg. All you have left to do now is pickup every recording that spawns. There are several of these and they are easy to miss, so be careful. The first one will spawn down by citadel. These will look like a regular drop but will have a pair of headphones inside of it instead. Once you pick one up, your screen will have a black and white filter on it and you will hear an audio cue. You must wait until this is complete before the next one spawns. These drops will lead you back up to the Infirmary where you will be able to complete the final step.</p>
                <hr>

                <a name = "step_six"></a>
                <h3>Step 6: Taking the Plane (Again)</h3>
                <p>After all of the recordings from the previous step are complete you need to end the round and wait for the next round transition to complete. Once this is done, enter the Afterlife and run up to the plane on the roof. Despite not having built the plane, you will see the plane there in ghost form.</p>
                <h4>Step 6 Tips</h4>
                <ul>
                    <li>After entering the Afterlife, you should see that there is no time limit for its use. If you see that the bar is ticking down, then you haven't listened to all of the audio tapes and need to revive yourself and fully complete Step 5.</li>
