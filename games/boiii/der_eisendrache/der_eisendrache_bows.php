                <h3>The Lightning Bow</h3>
                <h4>Binding The Bow</h4>
                <p>To begin upgrading the Wrath of the Ancients into the Lightning Bow, you must first bind the Lightning Bow upgrade path to your character. You can do this by shooting the weather vane up by the Death Ray. Doing this will play an audio clip and spawn a blue cloud. Interact with this cloud and the quest will be bound to you. After this, you're ready to begin the lighting of the bonfires.</p>
                <h4>Lighting The Bonfires</h4>
                <p>The second step in obtaining the Lightning Bow is lighting three bonfires spread across the map. This step <i>can</i> be difficult, so it is a good idea to do this toward the end of a round. The locations for these bonfires can be found below.</p>
                <ul>
                    <li>Bonfire 1: To the right of the Clock Tower, outside the map.</li>
                    <li>Bonfire 2: Above the Rocket Test Site, on a mountain outside the map.</li>
                    <li>Bonfire 3: Between the Rocket Test Site and the castle, on a hill outside the map.</li>
                </ul>
                <h4>Wall-Running</h4>
                <p>Now that you've completed the lighting of the bonfires, you're ready to move onto the wall-running step. This part is fairly easy and can be done anytime. Down in the under croft of the castle there are several symbols on the walls. To complete your step, you must run on the five oval shaped symbols on the wall <i>without</i> touching the ground. If done successfully, a sound will play, letting you know that you can move onto the next step.</p>
                <h4>Charging the Vases</h4>
                <p>After you've completed the wall-running, you are ready to move onto the second to last step for the Lightning Bow. To complete this step, you must fill up three different electrical vases found throughout the map with souls. After that is complete, you can obtain an electrical shot by drawing your bow and sticking the arrow into the vase. You must then shoot this charged arrow at the bonfires that were lit earlier in this process.</p>
                <ul>
                    <li>Electrical Vase 1: Inside the Clock Tower under the stairs.</li>
                    <li>Electrical Vase 2: In the room above Double Tap, next to the buildable table.</li>
                    <li>Electrical Vase 3: At the Rocket Test Site next to the pile of scrap in front of the teleporter.</li>
                </ul>
                <h4>Charging The Bow</h4>
                <p>Now that you've completed the last step of building the Lightning Bow, you can collect the lightning arrow. This arrow will be found where you initially bound the Lightning Bow quest to your character. Go to the blue cloud again and interact with it. After you've done that, you can go down to the pyramid room and put your arrow into its' pedestal. You will then need to kill zombies near the pedestal (within the dip in the floor) to charge the bow up. After this step is complete, you will hear another audio cue, indicating that your bow is ready to be picked up.</p>

