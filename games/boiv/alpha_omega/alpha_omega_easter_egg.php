                <a name = "step_two"></a>
                <h3>Step 2: Activating Rushmore</h3>
                <p>After turning on Pack-A-Punch, you can begin performing all of Rushmore's tasks. To find Rushmore, enter the <i>Operations</i> room (the first house on the right when you jump off the spawn ramp) and click on the computer in the back of the room. Rushmore will wake up and begin to give you instructions for necesssary steps to perform.</p>
