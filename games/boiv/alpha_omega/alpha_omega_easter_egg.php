                <a name = "step_seven"></a>
                <h3>Step 7: Deliver Rushmore His Server</h3>
                <p>After interacting with Rushmore, you will be told that there is a secret server hidden somewhere in Camp Edward that he needs you to find and bring to him. This location is to the right of the teleporter in <i>Power House</i>. Before beginning this step, make sure you have built the teleporter and have placed one pad in <i>Power House</i> and one in the backyard of <i>Prisoner Holding</i>. At this point, you need to have a Jolting Jack (the blue Nova 6 crawlers) shoot a bolt of electricity at the server and activate it. This part is a bit tricky, so it might take some time. Once this is done, you need to pull out your Path of Sorrows and enter the teleporter pad. Once you reach the backyard of <i>Prisoner Holding</i>, you need to go into the building and run through the side entrance into <i>Operations</i> and place the server into the server rack.</p>
