                        <li>You can complete all of these steps on Round 1 in a multiplayer game if you use a Shopping Free elixir and have a friend turn on power.</li>
                        <li>Try to save killing the normal zombies until the end of the round, this will make maximizing your points easier. It's okay to kill the Nova 6 enhanced zombies as they don't count toward the round cap while in the generator room.</li>
                        <li>In a multiplayer game, to maximize points, have one person bring the Dividend Yield elixir. By using this elixir, one person can kill all of the zombies and share the points with other players, so long as they're nearby.</li>
