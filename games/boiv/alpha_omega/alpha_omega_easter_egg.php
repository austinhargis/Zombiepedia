                <a name = "step_three"></a>
                <h3>Step 3: Finding Staff Codes</h3>
                <p>There are a total of four staff codes that you must find to feed into Rushmore. Each of these codes correspond to a different name, so make sure you enter the correct code when Rushmore asks for it. The first code will always be located in the same spot in the <i>Operations</i> house and will always be the same code (7626). It will be on a table to the left of you when you walk into <i>Operations</i> from the front door. Don't put this code in until you've collected the other three codes as you will have a time limit of one minute to successfully put them in.</p>
