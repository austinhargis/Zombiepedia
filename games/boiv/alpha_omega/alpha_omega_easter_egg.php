                <a name = "step_four"></a>
                <h3>Step 4: Find Clock Codes</h3>
                <p>In order to complete Step 4, you need to have galvaknuckles (purchaseable from the moving truck). Once you have these, find a staticy TV in <i>Beds</i>, <i>Diner</i>, or <i>Lounge</i>. Once here, you must kill a zombie with the galvaknuckles in front of the screen to activate it. At this point, be ready to write down the codes or take a picture of them as they are given quite quickly. These codes will be five characters long and indicate a location and a time. The location indicates a specific house. Within that house, you must find a clock and set that clock to the time given (e.g. A1030 would be house A with the clock set to 10:30). These codes <i>must</i> be put into the clocks in the order that they were given or you will fail this step and have to wait a round to try again. After inputting all of the codes, go back to Rushmore and interact with him. Despite what Rushmore says at this point, you have successfully completed this step and can move onto Step 5. Please note that at this point, you must interact with Rushmore after completing every step to start the next one.</p>
                    <h4>Easter Egg Tips</h4>
                    <ul>
                        <li>After completing this step, a security case will open on the second floor of <i>Operations</i>, revealing a free packed Raygun MKII.</li>
                        <li>This step isn't time limited, so feel free to take as long as you want.</li>
                        <li>Subtitles will help make this step easier as the codes will be read aloud and all players will see the codes printed out on their screen.</li>
                    </ul><hr>

                <a name = "step_five"></a>
                <h3>Step 5: Escort the Red Nova Crawler</h3>
                <p>Once starting this step, a red Nova 6 crawler will spawn somewhere in the basement. Upon finding this crawler, you will need to escort it to the Transfusion Facility. Upon entering this building, walk up to the barrier with stairs behind it and the crawler should crawl through the door and walk up the stairs. If you kill the crawler on this step, you will have to wait until next round for a new one to spawn.
                    <h4>Easter Egg Tips</h4>
                    <ul>
                        <li>Try to avoid killing any zombies during this time. Though you might not be aiming at the Nova 6 crawler, it is still possible to kill it.</li>
                    </ul><hr>

                <a name = "step_six"></a>
                <h3>Step 6: Find the Intruder</h3>
                <p>After interacting with Rushmore, head on down to <i>Storage</i> and activate A.D.A.M and lead him to the stairwell between <i>Diner</i> and the backyard of <i>Green House</i>. Once you reach this location, you will hear a voice from inside the door on the stairwell begin to speak and he will throw a 115 canister outside the door. Pick this up and take it to the <i>Transfusion Facility</i> and put it on the shelf. After this, return to Rushmore and talk to him again.</p>
