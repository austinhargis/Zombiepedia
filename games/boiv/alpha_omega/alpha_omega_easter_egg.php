                <a name = "step_eleven"></a>
                <h3>Step 11: Escort the Orb</h3>
                <p>Once you interact with Rushmore, it is time to find a blue glowing ball hidden somewhere out of bounds in the map. There are a total of three locations that it can spawn:</p>
                <ul>
                    <li>In a window in <i>Beds</i></li>
                    <li>In a window in the <i>Kitchen</i> of <i>Green House</i></li>
                    <li>In a window in the <i>Office</i> of <i>Yellow House</i></li>
                </ul>
                <p>Once you find this orb, it will begin to move and you <i>must</i> follow it. If at any time you lose the orb, it will despawn and you will have to find it again at the start of the next round. The orb's final destination will be in the <i>APD Interrogation</i> room. After a short discussion between the mannequin and Rushmore, you are set to start the next step.</p>
                <hr>

                <a name = "step_twelve"></a>
                <h3>Step 12: Boss Fight</h3>
                <p>To start the boss fight, all players must be down in <i>APD Control</i> and must interact with the computer. After doing this, the final part of this Easter Egg will begin. The first part of this is another lockdown. You must kill waves of mannequins around the pyramid, filling up soul canisters and unlocking the pyramid. After completing this part, you will meet an old (or new!) friend who will soon begin to attack you. You will need to fill up soul canisters in different parts of the basement, with each becoming closed to your access after you fill up that canister. The locations of these canisters (plus the recommended route) are as follows:</p>
                    <ul>
                        <li>Storage</li>
                        <li>Diner</li>
                        <li>Lounge</li>
                        <li>Beds</li>
                    </ul>
                <p>To start the process of filling these soul canisters, all players must be in the room before it can be interacted with. Once you begin to fill these canisters, pay attention to the audio logs and subtitles because you will need to escape the room as soon as the step is done. If you remain in these rooms, you will perish. Repeat these steps for each room. After completing every room, you need to return to <i>APD Control</i> and push Avogadro back into the pyramid. At this point, you are basically done with the Easter Egg. Your last step is to grab the Elemental Shard from the pyramid.</p>

                <h4>Easter Egg Tips</h4>
                <ul>
                    <li>For the best chance of success, we suggest that you complete this step in the order listed about (Storage, Diner, Loung, Beds).</li>
