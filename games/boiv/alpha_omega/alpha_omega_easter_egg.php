                    <li>Down in <i>Beds</i></li>
                    <li>Down in <i>Lounge</i></li>
                    <li>On the staircase leading to the second floor of <i>Green House</i></li>
                </ul>
                <p>You need to get a Brain Rot zombie to knock the paintings of the wall, which will reveal a code. Each of these three codes needs to be put into Rushmore.</p>
