                <a name = "step_nine"></a>
                <h3>Step 9: Restore Power to Camp Edward</h3>
                <p>This step requires you to interact with six different power switches distributed throughout the map. The locations and the orientation of the switches are as follows:</p>
                <ul>
                    <li>Beds - UP</li>
                    <li>Diner - UP</li>
                    <li>Solitary - DOWN</li>
                    <li>Generators - UP</li>
                    <li>Storage - DOWN</li>
                    <li>Lounge - DOWN</li>
                </ul>
                <p>This step does not seem to be time limited, so take as much time as you want.</p>
                <hr>

                <a name = "step_ten"></a>
                <h3>Step 10: Build a New Body</h3>
                <p>At the start of this step, three new mannequins will spawn in the map. The locations of which they can spawn are as follows:</p>
                    <ul>
                        <li>In the backyard of any house</li>
                        <li>In <i>Diner</i></li>
                        <li>In <i>Lounge</i></li>
                    </ul>
                    <p>You will need to find a specific mannequin that is sparking. Once you find it, make sure all players are in this area and interact with it. This will start a lockdown that you will need to survive to gain access to a part for rebuilding the mannequin located in <i>APD Interrogation</i>. Make sure you grab the body part after the lockdown is complete. Repeat this step another two times to gain access to two more body parts before finally returning to <i>APD Interrogation</i> and putting the parts on the mannequin.</p>
